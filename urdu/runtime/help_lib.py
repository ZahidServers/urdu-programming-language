def _fuzzy_suggest(query: str) -> None:
    query_low = query.lower()
    matches = [
        k for k in list(HELP.keys()) + list(CATEGORIES.keys())
        if query_low in k.lower() or k.lower() in query_low
    ]
    print(f"  ✗ '{query}' نہیں ملا")
    if matches:
        print(f"  شاید آپ کا مطلب: {', '.join(_cyan(m) for m in matches[:5])}")
    print(f"  تمام زمرے: {', '.join(_bold(k) for k in CATEGORIES)}")
    print(f"  مدد()  — تمام موضوعات")


def مدد(موضوع: str = None) -> None:
    """اردو مدد گار — Help system entry point."""
    if موضوع is None:
        _show_all()
    elif موضوع in CATEGORIES:
        _show_category(موضوع)
    elif موضوع in HELP:
        _show_topic(موضوع)
    else:
        _fuzzy_suggest(موضوع)


# English alias
