# ─── Display engine ──────────────────────────────────────────────────────────

def _line(width: int = 44) -> str:
    return "─" * width

def _header(text: str) -> str:
    w = 44
    inner = f"  {text}  "
    pad = max(0, w - len(inner))
    return (
        "╔" + "═" * (w) + "╗\n"
        f"║{inner}{' ' * pad}║\n"
        "╚" + "═" * (w) + "╝"
    )


def _show_all() -> None:
    print(_bold(_header("اردو پروگرامنگ لینگویج — مدد گار")))
    print()
    print(_dim("  مدد(\"زمرہ\")    — زمرے کے موضوعات"))
    print(_dim("  مدد(\"موضوع\")  — مخصوص موضوع کی تفصیل"))
    print()

    for cat_key, cat_info in CATEGORIES.items():
        رنگ = cat_info["رنگ"]
        topics = [k for k, v in HELP.items() if v["ز"] == cat_key]
        if not topics:
            continue
        print(f"  {رنگ(_bold(cat_key))}  —  {_dim(cat_info['عنوان'])}")
        # wrap topics at ~60 chars
        line, cols = "    ", 0
        for t in topics:
            if cols + len(t) + 2 > 58:
                print(line)
                line, cols = "    ", 0
            line += _cyan(t) + "  "
            cols += len(t) + 2
        if line.strip():
            print(line)
        print()

    print(_dim("  CLI:   python -m urdu مدد <موضوع>"))
    print(_dim("  REPL:  مدد(\"موضوع\")"))


def _show_category(cat_key: str) -> None:
    if cat_key not in CATEGORIES:
        _fuzzy_suggest(cat_key)
        return

    cat_info = CATEGORIES[cat_key]
    رنگ = cat_info["رنگ"]
    print()
    print(f"  {_bold(رنگ('══ ' + cat_info['عنوان'] + ' ══'))}")
    print()

    for name, entry in HELP.items():
        if entry["ز"] != cat_key:
            continue
        print(f"  {_bold(_cyan(name))}")
        print(f"    {entry['خ']}")
        for ln in entry["م"].split("\n"):
            print(_dim(f"    {ln}"))
        print()


def _show_topic(topic: str) -> None:
    if topic not in HELP:
        _fuzzy_suggest(topic)
        return

    entry = HELP[topic]
    cat_key = entry["ز"]
    cat_info = CATEGORIES.get(cat_key, {})
    رنگ = cat_info.get("رنگ", _white)

    print()
    print(f"  {_bold(_cyan(topic))}")
    print(f"  {_dim('زمرہ:')}  {رنگ(cat_info.get('عنوان', cat_key))}")
    print(f"  {_dim('خلاصہ:')} {entry['خ']}")
    print()
    print(f"  {_bold('مثال:')}")
    for ln in entry["م"].split("\n"):
        print(_green(f"    {ln}"))
    print()
    print(_dim("  مزید: مدد(\"" + entry['ز'] + "\")"))


def _fuzzy_suggest(query: str) -> None:
    query_low = query.lower()
    matches = [
        k for k in list(HELP.keys()) + list(CATEGORIES.keys())
        if query_low in k.lower() or k.lower() in query_low
    ]
    print(f"  ✗ '{query}' نہیں ملا")
    if matches:
        print(f"  شاید آپ کا مطلب: {', '.join(_cyan(m) for m in matches[:5])}")
    print(f"  تمام زمرے: {', '.join(_bold(k) for k in CATEGORIES)}")
    print(f"  مدد()  — تمام موضوعات")


def مدد(موضوع: str = None) -> None:
    """اردو مدد گار — Help system entry point."""
    if موضوع is None:
        _show_all()
    elif موضوع in CATEGORIES:
        _show_category(موضوع)
    elif موضوع in HELP:
        _show_topic(موضوع)
    else:
        _fuzzy_suggest(موضوع)


# English alias
help_urdu = مدد
